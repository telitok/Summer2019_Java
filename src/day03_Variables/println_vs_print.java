package day03_Variables;

public class println_vs_print {
	
	public static void main(String[] args) {
		
		// The only characters we can use to give name to a class are _ and $
		
		System.out.println("Hello Cybertek");
		System.out.println("School");
		
		// println is printing messages in the next line
		
		System.out.println("===================");
		
		
		System.out.print("Hello Cybertek");
		System.out.print("School");
		System.out.println("");
		
		// print is printing messages on the same line, directly where fist message ends
		
		
		System.out.println("===================");
		
		
		System.out.println("Today is ");
		System.out.print("a great day");

	}

}
